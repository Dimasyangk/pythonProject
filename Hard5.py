import math


def main(y, z):
    n = len(y)
    res = 0.0
    for i in range(1,n+1):
        num1 = z[i-1] + 38 * pow(y[math.ceil(i/3)-1], 3) + pow(y[math.ceil(i/3)-1], 2)/65
        num2 = math.log10(num1)**7
        num3 = num2/16
        res += 66 * num3
    return res


